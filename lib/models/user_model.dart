import 'dart:html';

class User {
  final int id;
  final String name;
  final String username;
  final int phone;
  final String category;
  final String dp;
  final String bio;

  User({
    required this.id,
    required this.name,
    required this.username,
    required this.phone,
    required this.category,
    required this.dp,
    required this.bio,
  });
}
